//! Инициализация трейсинга (логи/диагностика).
//!
//! Тонкая обёртка над `tracing-subscriber`: ставит глобальный подписчик с
//! фильтром уровней из окружения. По умолчанию — `info`; уровень переопределяется
//! переменной `RUST_LOG` (например, `RUST_LOG=app=debug,storage=info`).
//!
//! Вызов идемпотентен и не паникует: если подписчик уже установлен (например, в
//! тесте или при повторном входе), функция просто сообщает об этом через `bool`.

use tracing_subscriber::{fmt, EnvFilter};

/// Имя переменной окружения с директивами фильтрации уровней.
pub const FILTER_ENV: &str = "RUST_LOG";

/// Установить глобальный подписчик трейсинга.
///
/// Возвращает `true`, если подписчик установлен этим вызовом, и `false`, если
/// глобальный подписчик уже был задан ранее (повторная инициализация — no-op).
pub fn init() -> bool {
    // EnvFilter::from_env читает `RUST_LOG`; при отсутствии/ошибке — уровень `info`.
    let filter = EnvFilter::try_from_env(FILTER_ENV).unwrap_or_else(|_| EnvFilter::new("info"));

    fmt()
        .with_env_filter(filter)
        .with_target(true)
        .try_init()
        .is_ok()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn init_is_idempotent() {
        // Первый вызов может как установить подписчик, так и обнаружить уже
        // установленный (порядок тестов в процессе не гарантирован). Главное —
        // повторный вызов не паникует и сообщает, что глобальный подписчик занят.
        let _ = init();
        assert!(!init(), "повторная инициализация должна быть no-op");
    }
}
