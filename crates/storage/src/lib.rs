//! Локальное аналитическое хранилище.
//!
//! Накапливает котировки/обороты во времени, чтобы считать тренды и перетоки
//! денег (а не только мгновенный снимок). Движок — встраиваемый **DuckDB**
//! (колоночный OLAP, читает/пишет Parquet), идеально подходящий для запросов
//! вида «оборот по секторам по дням».
//!
//! ## Состав (Фаза 1)
//!
//! - [`schema`] — DDL таблиц («источник правды» по структуре БД);
//! - [`Db`] — соединение с DuckDB и применение миграций при открытии;
//! - [`migrate`] — версионированные миграции схемы;
//! - [`ingest`] — запись инструментов, баров, снимков оборота, классификации;
//! - [`query`] — аналитические запросы (оборот по секторам, топ-движения, потоки);
//! - [`TimeFrame`] — тайм-фрейм бара и его строковый код в БД.
//!
//! Планировщик батч-поллинга и бэкфилл (§ 1.4, § 1.6) живут в слое `app` и
//! подключаются вместе с сетевым адаптером `data` (Фаза 0 — gRPC), используя
//! примитивы записи из [`ingest`].

pub mod db;
pub mod ingest;
pub mod migrate;
pub mod query;
pub mod schema;
mod timeframe;

pub use db::Db;
pub use timeframe::TimeFrame;

/// Ошибки хранилища.
#[derive(Debug, thiserror::Error)]
pub enum StorageError {
    #[error("ошибка БД: {0}")]
    Db(String),
    #[error("миграция не применена: {0}")]
    Migration(String),
}

impl From<duckdb::Error> for StorageError {
    fn from(e: duckdb::Error) -> Self {
        StorageError::Db(e.to_string())
    }
}

/// Удобный псевдоним результата для операций хранилища.
pub type Result<T> = std::result::Result<T, StorageError>;
